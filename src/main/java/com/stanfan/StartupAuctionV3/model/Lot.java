package com.stanfan.StartupAuctionV3.model;

public class Lot {
	private int lotId;
	private int bidId;
	
	public Lot() {
		
	}
	
	
	
	public int getLotId() {
		return lotId;
	}
	public void setLotId(int lotId) {
		this.lotId = lotId;
	}
	public int getBidId() {
		return bidId;
	}
	public void setBidId(int bidId) {
		this.bidId = bidId;
	}
	

}
